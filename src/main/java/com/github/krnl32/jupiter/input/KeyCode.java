package com.github.krnl32.jupiter.input;

public enum KeyCode {
	// Keys
	UNKNOWN(0),
	SPACE(0x20),
	APOSTROPHE(0x27),
	COMMA(0x2C),
	MINUS(0x2D),
	PERIOD(0x2E),
	SLASH(0x2F),

	// Numeric Keys
	N0(0x30), N1(0x31), N2(0x32), N3(0x33), N4(0x34),
	N5(0x35), N6(0x36), N7(0x37), N8(0x38), N9(0x39),

	// More Keys
	SEMICOLON(0x3B),
	EQUAL(0x3D),

	// Letters
	A(0x41), B(0x42), C(0x43), D(0x44), E(0x45), F(0x46),
	G(0x47), H(0x48), I(0x49), J(0x4A), K(0x4B), L(0x4C),
	M(0x4D), N(0x4E), O(0x4F), P(0x50), Q(0x51), R(0x52),
	S(0x53), T(0x54), U(0x55), V(0x56), W(0x57), X(0x58),
	Y(0x59), Z(0x5A),

	// Brackets and Misc
	LEFT_BRACKET(0x5B),
	BACKSLASH(0x5C),
	RIGHT_BRACKET(0x5D),
	GRAVE_ACCENT(0x60),

	// Non-US
	WORLD_1(0xA1),
	WORLD_2(0xA2),

	// Function Keys
	ESCAPE(0x100), ENTER(0x101), TAB(0x102), BACKSPACE(0x103),
	INSERT(0x104), DELETE(0x105), RIGHT(0x106), LEFT(0x107),
	DOWN(0x108), UP(0x109), PAGE_UP(0x10A), PAGE_DOWN(0x10B),
	HOME(0x10C), END(0x10D), CAPS_LOCK(0x118), SCROLL_LOCK(0x119),
	NUM_LOCK(0x11A), PRINT_SCREEN(0x11B), PAUSE(0x11C),
	F1(0x122), F2(0x123), F3(0x124), F4(0x125), F5(0x126),
	F6(0x127), F7(0x128), F8(0x129), F9(0x12A), F10(0x12B),
	F11(0x12C), F12(0x12D), F13(0x12E), F14(0x12F), F15(0x130),
	F16(0x131), F17(0x132), F18(0x133), F19(0x134), F20(0x135),
	F21(0x136), F22(0x137), F23(0x138), F24(0x139), F25(0x13A),

	// Keypad
	KP_0(0x140), KP_1(0x141), KP_2(0x142), KP_3(0x143), KP_4(0x144),
	KP_5(0x145), KP_6(0x146), KP_7(0x147), KP_8(0x148), KP_9(0x149),
	KP_DECIMAL(0x14A), KP_DIVIDE(0x14B), KP_MULTIPLY(0x14C), KP_SUBTRACT(0x14D),
	KP_ADD(0x14E), KP_ENTER(0x14F), KP_EQUAL(0x150),

	// Modifiers
	LEFT_SHIFT(0x154), LEFT_CONTROL(0x155), LEFT_ALT(0x156), LEFT_SUPER(0x157),
	RIGHT_SHIFT(0x158), RIGHT_CONTROL(0x159), RIGHT_ALT(0x15A), RIGHT_SUPER(0x15B),
	MENU(0x15C);

	private final int code;

	KeyCode(int code) {
		this.code = code;
	}

	public int getCode() {
		return code;
	}

	public static KeyCode fromCode(int code) {
		for (KeyCode keyCode : KeyCode.values()) {
			if (keyCode.code == code) {
				return keyCode;
			}
		}
		return UNKNOWN;
	}
}
